export const BrandIcon = ({
  brandName,
  ...rest
}: IconProps & { brandName: string }) => {
  switch (brandName) {
    case "apple":
      return <Apple {...rest} />;
    case "xiaomi":
      return <Xiaomi {...rest} />;
    case "samsung":
      return <Samsung {...rest} />;
    default:
      return null;
  }
};

export const OptionIcons = ({
  os,
  prop,
  ...rest
}: IconBaseProps & { os: string; prop: string }) => {
  switch (prop) {
    case "ram":
      return <FaMicrochip {...rest} />;
    case "storage":
      return <MdStorage {...rest} />;
    case "batterie":
      return <MdBatteryChargingFull {...rest} />;
    case "resolution":
      return <CgScreen {...rest} />;
    case "CPU":
      return <FiCpu {...rest} />;
    case "cameras":
      return <AiOutlineCamera {...rest} />;
    case "screenSize":
      return <CgScreenShot {...rest} />;
    case "OS":
      return os.toLowerCase().includes("ios") ? (
        <SiIos {...rest} />
      ) : (
        <AiFillAndroid {...rest} />
      );
    default:
      return null;
  }
};
